package com.sonata;

public class ArithmeticOps {

	public static void main(String[] args) {
		float a = 12, b = 5;

	    
	    System.out.println("a + b = " + (a + b));

	    System.out.println("a - b = " + (a - b));

	    System.out.println("a * b = " + (a * b));

	    System.out.println("a / b = " + (a / b));
	    
	    System.out.println("a % b = " + (a % b));
	    
	    System.out.println(b/a);

	}

}
